package Array;

public class SimpleArray {

	public static void main(String[] args) {
		int[] number = new int[3];
		int number1[] = {82,74,95};
		number[0] = 82;
		number[1] = 74;
		number[2] = 95;
		
		for(int i = 0 ; i<3; i++) {
			System.out.println(number1[i]);
			System.out.println(number[i]);
		}
	}

}
