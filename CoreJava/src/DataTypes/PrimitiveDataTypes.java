package DataTypes;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
	
	int a = 52344;
	byte b = 15;
	short s=20;
	long l=512378L;
	char c ='H';
	float f = 12345.2f;
	double d =12334.33;
	boolean bl = true;
	 
	System.out.println(" 1 Int : "+a);
	System.out.println(" 2 Bytes : "+b);
	System.out.println(" 3 Short : "+s);
	System.out.println(" 4 Long : "+l);
	System.out.println(" 5 Character : "+ c);
	System.out.println(" 6 Float : "+f);
	System.out.println(" 7 Double : "+d);
	System.out.println(" 8 Boolean : "+bl);
	}

}
